-- Name change trigger

CREATE OR REPLACE FUNCTION name_notification()
  RETURNS TRIGGER AS $name_note$
BEGIN
  IF (TG_OP = 'DELETE')
  THEN
    INSERT INTO notification (id, version, message, object_id)
      SELECT
        nextval('hibernate_sequence'),
        0,
        'name deleted',
        OLD.id;
    RETURN OLD;
  ELSIF (TG_OP = 'UPDATE')
    THEN
      INSERT INTO notification (id, version, message, object_id)
        SELECT
          nextval('hibernate_sequence'),
          0,
          'name updated',
          NEW.id;
      RETURN NEW;
  ELSIF (TG_OP = 'INSERT')
    THEN
      INSERT INTO notification (id, version, message, object_id)
        SELECT
          nextval('hibernate_sequence'),
          0,
          'name created',
          NEW.id;
      RETURN NEW;
  END IF;
  RETURN NULL;
END;
$name_note$
LANGUAGE plpgsql;


CREATE TRIGGER name_update
  AFTER INSERT OR UPDATE OR DELETE
  ON name
  FOR EACH ROW
EXECUTE PROCEDURE name_notification();

